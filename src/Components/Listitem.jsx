import TablePagination from '@mui/material/TablePagination';
import TableRow from '@mui/material/TableRow';
import { useState } from 'react';
import "./Listitem.css"

const columns = [
  { id: 'account', label: 'Address', minWidth: 170 },
  
  {
    id: 'balance',
    label: 'Balance',
    minWidth: 170,
    align: 'right',
    format: (value) => value.toFixed(5),
  }
  
];

